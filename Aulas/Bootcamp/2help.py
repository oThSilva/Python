# Em Python, a função help() é uma função integrada que fornece informações de ajuda sobre objetos, módulos, funções, classes e métodos. Ela é especialmente útil quando você precisa de documentação sobre como usar determinado objeto ou entender o propósito de uma função específica.
# Sintaxe:

# No modo interativo:
help()
help > print

# Para obter ajuda sobre a função built-in 'print':
help(print)

# Para obter ajuda sobre a lista:
help(list)

# Para obter ajuda sobre um objeto específico:
meu_objeto = "exemplo"
help(meu_objeto)
