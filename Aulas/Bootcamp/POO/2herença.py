# Herança simples: Quando uma classe herda apenas uma classe pai.


class a:  # classe base, ou classe pai
    pass


class b(a):  # classe filha da classe a
    pass


# Heranla múltipla: Quando uma classe herda várias classes pai.


class a1:
    pass


class b1:
    pass


class c1(a1, b1):
    pass
